# STRING QUESTION

print('\n**********Using isalnum() function on STRING**********')
str1 = input('Enter the string :: ')
print(str1.isalnum())


print("\n***********************************************************")

print('**********Using isdigit() function on STRING**********')
str2 = input('Enter the string :: ')
print(str2.isdigit())


print("\n***********************************************************")

print('**********Using isspace() function on STRING**********')
str3 = input('Enter the string :: ')
print(str3.isspace())


print("\n***********************************************************")

print('**********Using rindex() function on STRING**********')
str4 = input('Enter the string :: ')
search = input('Enter the string you want to search :: ')
print(str4.rindex(search))


print("\n***********************************************************")

print('**********Using lstrip() function on STRING**********')
str5 = input('Enter the string :: ')
